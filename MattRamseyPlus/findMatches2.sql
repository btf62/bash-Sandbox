/* 6) Tag lookup + guard */
DECLARE @TagName NVARCHAR(200) = 'Ramsey Plus';
DECLARE @TagId INT;
DECLARE @EntityTypeId INT;

SELECT
    @TagId = t.Id,
    @EntityTypeId = t.EntityTypeId
FROM Tag t
WHERE t.Name = @TagName;

SELECT @TagId AS TagIdFound, @EntityTypeId AS EntityTypeIdFound;

IF @TagId IS NULL OR @EntityTypeId IS NULL
BEGIN
    SELECT 'ERROR: Tag not found or missing EntityTypeId' AS ErrorMessage, @TagName AS TagName;
    RETURN;
END


/* 7) Apply tag to HIGH only, duplicate-safe */
DECLARE @Inserted TABLE (EntityGuid UNIQUEIDENTIFIER);

INSERT INTO TaggedItem (
    TagId,
    EntityTypeId,
    EntityGuid,
    [Guid],
    IsSystem,
    CreatedDateTime,
    ModifiedDateTime
)
SELECT
    @TagId,
    @EntityTypeId,
    p.[Guid],
    NEWID(),
    0,
    GETDATE(),
    GETDATE()
FROM #RamseyImport ri
JOIN Person p
    ON p.Id = ri.MatchedPersonId
WHERE ri.MatchConfidence IN ('High', 'Low')
  AND NOT EXISTS (
      SELECT 1
      FROM TaggedItem ti
      WHERE ti.TagId = @TagId
        AND ti.EntityGuid = p.[Guid]
  );

SELECT COUNT(*) AS NewlyTaggedThisRun
FROM @Inserted;


