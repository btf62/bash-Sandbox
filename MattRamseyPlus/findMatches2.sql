/* 2) HIGH: Email match, UNIQUE only */
;WITH EmailMatches AS (
    SELECT
        ri.ImportRowId,
        p.Id AS PersonId,
        COUNT(*) OVER (PARTITION BY ri.ImportRowId) AS MatchCount
    FROM #RamseyImport ri
    JOIN Person p
      ON LOWER(LTRIM(RTRIM(p.Email))) = LOWER(LTRIM(RTRIM(ri.Email)))
    WHERE ri.MatchedPersonId IS NULL
      AND ri.Email IS NOT NULL
      AND ri.Email <> ''
)
UPDATE ri
SET
    MatchedPersonId = em.PersonId,
    MatchConfidence = 'High'
FROM #RamseyImport ri
JOIN EmailMatches em
  ON em.ImportRowId = ri.ImportRowId
WHERE em.MatchCount = 1;


/* 3) LOW: Name match, UNIQUE only */
;WITH NameMatches AS (
    SELECT
        ri.ImportRowId,
        p.Id AS PersonId,
        COUNT(*) OVER (PARTITION BY ri.ImportRowId) AS MatchCount
    FROM #RamseyImport ri
    JOIN Person p
      ON LOWER(LTRIM(RTRIM(p.NickName))) = LOWER(LTRIM(RTRIM(ri.FirstName)))
     AND LOWER(LTRIM(RTRIM(p.LastName))) = LOWER(LTRIM(RTRIM(ri.LastName)))
    WHERE ri.MatchedPersonId IS NULL
      AND ri.FirstName IS NOT NULL AND ri.FirstName <> ''
      AND ri.LastName IS NOT NULL  AND ri.LastName <> ''
)
UPDATE ri
SET
    MatchedPersonId = nm.PersonId,
    MatchConfidence = 'Low'
FROM #RamseyImport ri
JOIN NameMatches nm
  ON nm.ImportRowId = ri.ImportRowId
WHERE nm.MatchCount = 1;


